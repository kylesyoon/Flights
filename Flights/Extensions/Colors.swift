import UIKit

extension UIColor {
    static func flyt_pink() -> UIColor {
        return UIColor(red: 1.0, green: 61.0 / 255.0, blue: 127.0 / 255.0, alpha: 1.0)
    }
    
    static func flyt_primaryGreen() -> UIColor {
        return UIColor(red: 63.0 / 255.0, green: 184.0 / 255.0, blue: 175.0 / 255.0, alpha: 1.0)
    }
    
    static func flyt_secondaryGreen() -> UIColor {
        return UIColor(red: 127.0 / 255.0, green: 199.0 / 255.0, blue: 175.0, alpha: 1.0)
    }
}
